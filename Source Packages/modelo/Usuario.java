package modelo;

public class Usuario {
    String mail;
    String pass;
    int id;

    public Usuario() {
    }

    public Usuario(String mail, String pass, int id) {
        this.mail = mail;
        this.pass = pass;
        this.id = id;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
